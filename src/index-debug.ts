import express, { Express } from 'express';
import dotenv from 'dotenv';

// Load environment variables first
dotenv.config();

console.log('рҹҡҖ Starting TaskFlow server...');
console.log('рҹ“Қ Node environment:', process.env.NODE_ENV || 'development');
console.log('рҹ“Қ Port:', process.env.PORT || 3000);

const app: Express = express();
const PORT = process.env.PORT || 3000;

console.log('вң… Express app created');

// Basic middleware first
app.use(express.json());
app.use(express.urlencoded({ extended: true }));

console.log('вң… Basic middleware loaded');

// Simple health check that doesn't require database
app.get('/health', (req, res) => {
  res.status(200).json({
    success: true,
    data: {
      status: 'healthy',
      timestamp: new Date().toISOString(),
      uptime: process.uptime(),
    },
  });
});

console.log('вң… Basic health check route added');

